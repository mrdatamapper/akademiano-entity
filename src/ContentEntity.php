<?php


namespace Akademiano\Entity;


class ContentEntity extends NamedEntity implements ContentEntityInterface
{

    protected $content;

    /**
     * @return mixed
     */
    public function getContent()
    {
        return $this->content;
    }

    /**
     * @param mixed $content
     */
    public function setContent($content)
    {
        $this->content = $content;
    }
}
